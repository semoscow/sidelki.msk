export default function BlogArticle() {
  const { slug } = useParams<{ slug: string }>()
  const article = articlesData[slug || '']

  if (!article) {
    return (
      <div className="bg-[#F7F3EE] py-20 text-center">
        <h2 className="font-display text-2xl text-[#2C241B] mb-4">Статья не найдена</h2>
        <Link to="/blog" className="text-[#1B3A2F] underline">Вернуться в блог</Link>
      </div>
    )
  }

  return (
    <div>
      <PageHero
        title={article.title}
        breadcrumbs={[
          { label: 'Главная', href: '/' },
          { label: 'Блог', href: '/blog' },
          { label: article.title },
        ]}
      />

      <article className="bg-[#F7F3EE] py-12 lg:py-16">
        <div className="max-w-4xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className="mb-6 flex items-center gap-4">
            <span className="text-xs font-medium uppercase tracking-wider text-[#C9A87C]">{article.tag}</span>
            <span className="text-xs text-[#9A8F80]">{article.date}</span>
          </div>

          <div className="mb-10">
            <img
              src={article.image}
              alt={article.title}
              className="w-full aspect-[16/9] object-cover"
            />
          </div>

          <div className="max-w-3xl mx-auto">
            {article.content.map((block, i) => {
              if (block.type === 'h2') {
                return (
                  <h2 key={i} className="font-display text-2xl text-[#2C241B] mt-10 mb-4">
                    {block.text}
                  </h2>
                )
              }
              if (block.type === 'p') {
                return (
                  <p key={i} className="text-[#6B5F4F] leading-relaxed mb-4">
                    {block.text}
                  </p>
                )
              }
              if (block.type === 'ul') {
                return (
                  <ul key={i} className="space-y-2 mb-6">
                    {block.items?.map((item, j) => (
                      <li key={j} className="flex items-start gap-3 text-[#6B5F4F]">
                        <span className="text-[#C9A87C] mt-1 shrink-0">•</span>
                        <span className="text-sm leading-relaxed">{item}</span>
                      </li>
                    ))}
                  </ul>
                )
              }
              return null
            })}
          </div>

          <div className="mt-12 pt-8 border-t border-[#D9D3CA] flex items-center justify-between">
            <Link to="/blog" className="text-sm font-medium text-[#1B3A2F] hover:text-[#C9A87C] transition-colors">
              ← Вернуться в блог
            </Link>
            <Link to="/uslugi" className="text-sm font-medium text-[#1B3A2F] hover:text-[#C9A87C] transition-colors">
              Все услуги →
            </Link>
          </div>
        </div>
      </article>
    </div>
  )
}
